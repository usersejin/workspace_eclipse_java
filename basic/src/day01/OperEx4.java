package day01;
// 4. 비교연산자
// >, <, >=, <=, ==, !=
public class OperEx4 {
	public static void main(String[] args) {
		int a = 100;
		int b = 20;
		
		System.out.println("a > b : " + (a > b)); // boolean 타입으로 나타냄(true, false)로 
		System.out.println("a < b : " + (a < b));  
		System.out.println("a >= b : " + (a >= b));  
		System.out.println("a <= b : " + (a <= b));  
		System.out.println("a == b : " + (a == b));  
		System.out.println("a != b : " + (a != b));  
	}
}
