package day01;

public class VarEx2 {
	public static void main(String[] args) {
		
		// 정수형 변수 i에 20값을 대입
		byte i = 20;
		// 값이 20이므로 메모리 절약을 위해 int 보다는 byte
		
		// 실수형 변수 h에 182.3 대입
		float h = 182.3f;
		// 이것도 double 보단 float 
		System.out.println("나이 : " + i);
		System.out.println("키 : " + h);
	}
}
